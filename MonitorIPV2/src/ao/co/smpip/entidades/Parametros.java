package ao.co.smpip.entidades;

public class Parametros {
	private int tempoTela;
	private double normalPosto;
	private double medianPosto;
	private double falhasPosto;
	private int id;

	public int getTempoTela() {
		return tempoTela;
	}

	public void setTempoTela(int tempoTela) {
		this.tempoTela = tempoTela;
	}

	public double getNormalPosto() {
		return normalPosto;
	}

	public void setNormalPosto(double normalPosto) {
		this.normalPosto = normalPosto;
	}

	public double getMedianPosto() {
		return medianPosto;
	}

	public void setMedianPosto(double medianPosto) {
		this.medianPosto = medianPosto;
	}

	public double getFalhasPosto() {
		return falhasPosto;
	}

	public void setFalhasPosto(double falhasPosto) {
		this.falhasPosto = falhasPosto;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
   
   
}
